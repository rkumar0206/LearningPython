a = 10.50
b = 5

# ADD ( + )

c = a + b
print('a + b = {}'.format(c))

# SUBTRACT ( - )

c = a - b
print('a - b = {}'.format(c))

# MULTIPLY ( * )

c = a * b
print('a * b = {}'.format(c))

# DIVISION

c = a / b
print('a / b = {}'.format(c))

# INTEGER DIVISION

c = a // b
print('a//b = {}'.format(c))

# EXPONENT

c = a ** b
print('a to the power b = {}'.format(c))

# REMAINDER

c = a % b
print('a % b = {}'.format(c))
