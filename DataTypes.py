# There are three major datatype i.e. int, float and String

i = 25  # int
f = 33.45  # float
s = 'this is a string'  # string

# Type casting

# int to float

iToF = float(i)
print("Int to float : {}".format(str(iToF)))

# float to int

fToi = int(f)
print("Float to int : {}".format(str(fToi)))

# int to string

iTos = str(i)
print("Int to String : {}".format(str(iTos)))

# string to int

s = 54
sToi = int(s)
print("String to Int : {}".format(str(sToi)))

